// ---------- 全类型长文自动补全（若缺失） ----------
// 目标：让所有类型都具备与 ENFP 一致的长文密度；当 profiles 中未手写 long 字段时，用下述模板组合而成

const sentence = (s: string) => (s.endsWith('。') || s.endsWith('！') || s.endsWith('？') ? s : s + '。')

function composeLongSection(section: ProfileSection): string {
  const desc = sentence(section.description || '')
  const adv = section.strengths?.length ? sentence(`优势包括：${section.strengths.join('、')}`) : ''
  const risk = section.challenges?.length ? sentence(`注意事项：${section.challenges.join('、')}`) : ''
  const act = section.tips?.length ? sentence(`实用建议：${section.tips.join('；')}`) : ''
  const more = '建议将上述要点落地为每周节奏：以小步快跑的方式持续交付，并在周度复盘中沉淀模板与清单，形成可复制机制。'
  return [desc, adv, risk, act, more].join('')
}

function composeJungLong(funcs: string[], fallback?: string): string {
  const order = funcs.join(' · ')
  const s1 = sentence(`你的功能顺序为：${order}`)
  const s2 = funcs[0] ? sentence(`主导功能优势：${funcs[0]}，请为其安排价值最高的任务场景`) : ''
  const s3 = funcs[1] ? sentence(`辅助功能优势：${funcs[1]}，在协作中可作为稳定输出的基座`) : ''
  const s4 = funcs[2] ? sentence(`第三功能常见盲点：${funcs[2]}，需要流程护栏、同伴审阅与时间缓冲`) : ''
  const s5 = funcs[3] ? sentence(`第四功能容易被忽视：${funcs[3]}，用低剂量训练与休息逐步补齐`) : ''
  const s6 = '每周一次功能平衡复盘：记录本周在哪些场景被某一功能过度驱动引发偏差，下周如何用其他功能对冲；将功能语言化（如"我现在在用Ni/Te…"）来提升团队协作。'
  return (fallback ? sentence(fallback) : '') + [s1, s2, s3, s4, s5, s6].join('')
}

function composeShadowLong(type: string, fallback?: string): string {
  const s1 = sentence(`在压力或能量透支时，可能短暂呈现 ${type} 的影子特征`) 
  const s2 = sentence('识别信号：以评判代替对话、在会议中频繁打断或情绪抽离、迷恋细节并否定当下创造')
  const s3 = sentence('应对策略：暂停输入做15分钟静息/步行；将任务缩减为30–60分钟原型；邀请可信同伴复述事实区分情绪；会后进行触发—行为—替代复盘')
  const s4 = '影子面并非敌人，而是提醒我们需要边界、节奏与复盘；把自我要求转化为系统化的支持，才能长期稳定输出。'
  return (fallback ? sentence(fallback) : '') + [s1, s2, s3, s4].join('')
}

function composeSummaryLong(tp: TypeProfile): string {
  const s1 = sentence(`你的核心价值：${tp.subtitle}`)
  const s2 = tp.career.strengths?.length ? sentence(`继续发扬：${tp.career.strengths.slice(0,4).join('、')}`) : ''
  const s3 = tp.career.challenges?.length ? sentence(`重点改善：${tp.career.challenges.slice(0,4).join('、')}`) : ''
  const s4 = '把优势场景与节奏固化为流程，把盲点转化为护栏；以"原型—反馈—复盘—沉淀"的闭环持续升级，长期来看你的价值会以复利形式增长。'
  return [s1, s2, s3, s4].join('')
}

