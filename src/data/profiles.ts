  const p = typeProfiles[k]
  // 不覆盖已人工撰写的 ENFP 等 long 字段
  if (!p.longCareer) p.longCareer = composeLongSection(p.career)
  if (!p.longRelationship) p.longRelationship = composeLongSection(p.relationship)
  if (!p.longSocial) p.longSocial = composeLongSection(p.social)
  if (!p.longPersonality) p.longPersonality = sentence(p.personality.analysis) + sentence(`核心特质：${p.personality.traits.join('、')}`)
